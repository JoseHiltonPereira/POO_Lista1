package exercicio_215;

public class Calculo {
	public void soma(int num1, int num2){
		System.out.println("Soma: " + (num1 + num2));
	}
	
	public void diferenca(int num1, int num2){
		System.out.println("Diferença: " + (num1 - num2));
	}
	
	public void produto(int num1, int num2){
		System.out.println("Produto: "+ (num1 * num2));
	}
	
	public void quociente(double num1, double num2){
		System.out.printf("Quociente: " + (num1 / num2));
	}
}
